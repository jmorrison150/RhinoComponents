    /// <summary>
    /// This procedure contains the user code. Input parameters are provided as regular arguments,
    /// Output parameters as ref arguments. You don't have to assign output parameters,
    /// they will have a default value.
    /// </summary>
    private void RunScript(Polyline polyline, ref object location, ref object text)
    {
        List<string> slopes = new List<string>();
        List<Point3d> midPoints = new List<Point3d>();

        for (int i = 1; i < polyline.Count; i++)
        {
            Point3d pt1 = polyline[i];
            Point3d pt0 = polyline[i - 1];
            Point3d midPoint = new Point3d((pt1 + pt0) * 0.5);
            double rise = Math.Abs(pt1.Z - pt0.Z);
            pt1.Z = 0.0;
            pt0.Z = 0.0;
            double run = Math.Abs(pt1.DistanceTo(pt0));
            double slope = 0.0;


            slope = (rise / run)*100.0;

            string txt = String.Format("{0:0}%", slope);
            slopes.Add(txt);
            midPoints.Add(midPoint);
        }

        text = slopes;
        location = midPoints;




    }

